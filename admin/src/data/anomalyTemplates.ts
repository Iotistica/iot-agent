import type { DetectionMethod, SeasonalityPattern } from '@/types'

/**
 * Built-in, site-agnostic anomaly rule presets. Ship as static data (not DB rows)
 * since they're the same for every install; custom ("save as template") templates
 * are DB-backed instead (see anomalyApi.getTemplates/createTemplate).
 *
 * Mapped from a domain-expert-curated table (building-management + industrial
 * presets). Mapping notes:
 *  - Source gives ranges (e.g. window "30-60", cooldown "10-15 min") — collapsed
 *    to a single representative value (midpoint, rounded); minutes converted to ms.
 *  - minConfidence isn't in the source table — defaulted to 0.7 for every entry
 *    (matches the system default used elsewhere, e.g. blankMetric()).
 *  - Seasonality only exists on the building-management rows in the source table;
 *    "Daily" -> 'hourly' (24 buckets capture a daily cycle), "Daily and weekly"
 *    -> 'weekly', "Occupancy schedule" -> 'hourly' (closest proxy; true
 *    schedule-awareness isn't implemented), "None" -> 'none'. Industrial rows have
 *    no seasonality column in the source and default to 'none'.
 *  - "Stuck-value logic" (valve/damper rows) isn't an implemented detector (no
 *    such method in DetectionMethod / getAllDetectors()) — dropped from methods,
 *    falls back to the IQR + rate_change the source already lists alongside it.
 *    Purpose text still mentions "stuck" since that's the operator-facing intent.
 *  - hasExpectedRange templates never carry numeric bounds — they're inherently
 *    site-specific. Selecting one still adds 'expected_range' to methods, which
 *    triggers the "fill in bounds" validation hint in the rule drawer.
 *  - "Power quality" was the last row of the source table and got cut off before
 *    threshold/window/cooldown were given. Filled with values matching the
 *    closest comparable row ("Building electrical demand") — flagged here for
 *    review since it's an assumption, not a value from the source table.
 */

